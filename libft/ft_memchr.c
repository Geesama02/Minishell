#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (n > i)
	{
		if ((unsigned char)c == *str)
			return ((void *)(s + i));
		str++;
		i++;
	}
	return (NULL);
}
